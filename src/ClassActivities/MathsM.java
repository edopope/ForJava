package ClassActivities;

public class MathsM {
    private int sum;
    public int Square(int x) {
        return x * x;
    }

    public int sumOfSquare(int a, int b) {
        int sum = a * b;
        int result = sum + sum;
        return result;
    }

    public int subtractOfSquare(int x, int y) {
        int sum = x * y;
        int result = sum - sum;
        return result;
    }

    }
